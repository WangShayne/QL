##设置编码语言 
SET NAMES gbk;
##删除已有的tarena库
DROP DATABASE IF EXISTS tarena;
##创建新的tarena库
CREATE DATABASE tarena CHARSET=gbk;
##使用tarena库
USE tarena;
##创建员工列表,包含员工编号,员工姓名,性别,生日,部门
CREATE TABLE emp(
	eno INT PRIMARY KEY AUTO_INCREMENT,
	ename VARCHAR(32),
	gender VARCHAR(32),
	birthday LONG,
	dept VARCHAR(32)
);
